#include <math.h>
#include <stdio.h>
#include <stdlib.h>
#include <string.h>
int main()
{
    int num,i;
    int remain;
    scanf("%d",&num);
    for (i=0;i<num;i++){
        int n,m,s;
        scanf("%d %d %d ",&n,&m,&s);
        
        remain=n%m;
        
    }
    printf("%d",remain);
}